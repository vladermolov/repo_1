// OPERAR CON VARIABLES 
// +   :   SUMA 
// -   :   RESTAR
// *   :   MULTIPLICAR 
// /   :   DIVISIÓN
// %   :   RESTO Ó MÓDULO
// ++  :   INCREMENTO EN UNA UNIDAD
// --  :   DECREMENTO EN UNA UNIDAD

// Ejemplos: 
var num1 = 10;
var num2 = 5;

var suma = num1 + num2;
var resta = num1 - num2;
var mult = num1 * num2;
var div = num1 / num2;
var resto = num1 % num2;

// Resultados por consola
console.log("La suma de " + num1 + " con " + num2 + " es " + suma);
console.log(resta);
console.log(mult);
console.log(div);
console.log(resto);















